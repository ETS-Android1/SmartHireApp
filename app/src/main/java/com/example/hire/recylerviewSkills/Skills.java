package com.example.hire.recylerviewSkills;

public class Skills {

    private String skill;
    private String level;

    public Skills() {
    }

    private void setSkill(String skill){
        this.skill = skill;
    }

    private String getSkill(){
        return skill;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getLevel() {
        return level;
    }

}
